class Array
  def hash
    result = 0
    self.each_with_index do |el, idx|
      result += el.hash * idx
    end

    result.hash
  end
end

class String
  def hash
    self.bytes.hash
  end
end

class Hash
  # This returns 0 because rspec will break if it returns nil
  # Make sure to implement an actual Hash#hash method
